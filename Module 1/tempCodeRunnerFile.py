# Convert an integer to a float
integer_number = 123
float_number = float(integer_number)
print("Integer Number:", integer_number, "Type:", type(integer_number))
print("Float Number:", float_number, "Type:", type(float_number))

# Convert a string to a float
string_number = "3.14"
float_number_from_string = float(string_number)
print("String Number:", string_number, "Type:", type(string_number))
print("Float Number from String:", float_number_from_string, "Type:", type(float_number_from_string))